// 魔力宝贝怀旧服 闲鱼商品分类器。
// 闲鱼搜索结果里"账号/宠物/装备/道具"经常混在一起，标题是唯一可靠信号，
// 因此按"先具体、再宽泛"的优先级逐条匹配。顺序很重要：
//   1) 服务/虚拟充值类（代练、点卡、游戏币）先摘出去
//   2) 首饰、道具这类商品词
//   3) 强账号信号（连证/龙证/坯子/双修/多ID）——放在宠物之前，
//      否则"龙证…带宠物"的角色号会被误判成宠物
//   4) 宠物（档位/树海/招牌/改造/具体宠物名）
//   5) 人物装备
//   6) 兜底归为游戏账号
export const CATEGORIES = [
  "游戏账号",
  "游戏币",
  "人物装备",
  "首饰",
  "宠物",
  "道具",
  "代练代肝",
  "点卡充值",
];

export function classify(title) {
  const t = String(title || "");

  // 1. 代练代肝
  if (/代练|代肝|代打|代刷|陪练|包升|升级服务|全程托管|托管解放|代冲|代烧|代做|带练|带任务/.test(t))
    return "代练代肝";

  // 2. 点卡充值
  if (/点卡|时长|月卡|季卡|年卡|直充|代充|充值|激活码|新手卡|易玩通|点数直/.test(t))
    return "点卡充值";

  // 3. 游戏币
  if (/魔币|金币|游戏币|元宝/.test(t)) return "游戏币";

  // 4. 首饰
  if (/戒指|项链|手镯|耳环|耳坠|首饰|饰品|之饰|护身符|魔戒|圣戒|戒\b/.test(t))
    return "首饰";

  // 5. 道具 / 材料 / 消耗品
  if (
    /血瓶|魔泉|血泉|药水|药剂(?!师)|卷轴|精华|龙珠|灵之匣|神炼|强化石|强化|符文|碎片|宝石|时水|锅子|寿喜锅|铁条|铜条|矿条|喜马拉斯|月木|银块|材料|栅栏|轻装|露比|经验|彩牛|物品袋|血泉|完修/.test(
      t,
    )
  )
    return "道具";

  // 6. 强账号信号（连证/龙证/坯子/双修/修理/多ID）——必须在宠物之前
  if (
    /连证|龙证|大连证|单证|\d+\s*连(?!击)|\d+\s*ID|\d+\s*id|坯子号|坯子|胚子号|胚子|修理号|双修|生产号|小龙证|过户|护士长|护士10|双偷|封印/.test(
      t,
    )
  )
    return "游戏账号";

  // 7. 宠物（改造宠/具体宠物名/宠物专属属性写法）
  if (
    /树海|档位|招牌|大赛|大奖|改造|改宠|纯血加点|纯魔加点|纯敏加点|混敏|不爆点|不爆敏|单只|\d+\s*只|迷你龙|布雷欧|希特拉|波加|帝皇|皮卡|奇美拉|史莱姆|幽冥之灵|树化石|地狱使者|机械核|气功蛋|分身|鄂牙|螳螂|兔子|蓝鼠|炎龙|哥布林|巨人|骷髅|冰牛|风牛|黑蛇|红鬼|白鬼|蓝鬼|青鬼|黄鬼|咒灵|不死系|散步|庞物|宠物|改迷你|改红|改白|改蓝|改杀龙|改镰刀|尸体|蛋\b|灵风|白狗|三号机/.test(
      t,
    )
  )
    return "宠物";

  // 8. 人物装备
  if (
    /武器|防具|铠甲|头盔|盾牌|披风|靴|手套|10[Cc]|9[Cc]|光辉|黑龙|镰刀|裂空|女王之刃|圣甲|套装|装备|法装|骑装/.test(
      t,
    )
  )
    return "人物装备";

  // 9. 兜底：游戏账号
  return "游戏账号";
}
