// 魔力宝贝怀旧服 闲鱼商品分类器（分层版 v3）。
//
// 闲鱼搜索里"账号 / 宠物 / 装备 / 道具 / 币"经常混在一条标题里，难点是
// 判断"卖的主体是什么"。按下面顺序逐层匹配，先具体后宽泛、先服务后实物：
//   1) 代练   2) 点卡   3) 首饰
//   4) 强账号（连证/龙证/坯子/单证…）——角色号即便带宠/带币也算账号
//   5) 宠物·结构（档位/招牌/树海/纯加点/X只/胞胎队/任务宠）
//   6) 人物装备·具体（光辉剑/10C法装/女王之刃…）
//   7) 道具·消耗材料（血泉/药/宝石/矿/材料/尸体…）——放在游戏币之前，
//      这样"魔泉血泉魔币锅子"这种消耗品清单不会被当成币
//   8) 游戏币（魔币/金币…，此时消耗品已摘走，可放宽）
//   9) 宠物·名称（迷你龙/史莱姆/哥布林/改造X…）
//   10) 人物装备·宽泛（装备/衣服/鞋帽/10C/9紫…）
//   11) 兜底 = 游戏账号
export const CATEGORIES = [
  "游戏账号", "游戏币", "人物装备", "首饰", "宠物", "道具", "代练代肝", "点卡充值",
];

export function classify(title) {
  // 砍掉卖家在尾部堆的"搜索关联词：金币 点卡 装备…"广告，避免污染分类。
  const t = String(title || "").split(/搜索关联词|搜索关键|关联词|搜索词|tag[:：]|标签[:：]/)[0];

  // 1. 代练代肝
  if (/代练|代肝|代打|代刷|陪练|包升|升级服务|托管|代冲|代烧|代做|代工|带练|带任务|带做|跟车|接任务|代过|刷声望|砍村服务|带砍/.test(t))
    return "代练代肝";

  // 2. 点卡充值
  if (/点卡|时长|月卡|季卡|年卡|直充|代充|充值|激活码|新手卡|易玩通|点数直/.test(t))
    return "点卡充值";

  // 3. 首饰
  if (/戒指|项链|手镯|手链|耳环|耳坠|首饰|饰品|之饰|护身符|魔戒|圣戒/.test(t))
    return "首饰";

  // 4. 强账号信号（结构性，优先于宠物/币，避免"带宠/带币的角色号"误判）
  if (/连证|龙证|大连证|单证|\d+\s*连(?!击)|\d+\s*ID|\d+\s*id|坯子|胚子|修理号|生产号|过户|小龙证|大生产账号|个人证|双偷封印|护士长账号|满级.{0,4}号/.test(t))
    return "游戏账号";

  // 5. 宠物·结构信号（在卖一只生物）
  if (/档位|档次|树海|招牌|大赛|大奖|纯血加点|纯魔加点|纯敏加点|混敏|不爆[点敏]|散步|胞胎队|胞胎|任务宠|\d+\s*只|单只|一队.{0,6}(龙|鬼|牛|蛇|龟|兔|鼠|螂|布林|史莱姆|马|猪|鸟)/.test(t))
    return "宠物";

  // 6. 人物装备·具体（具体装备名，先于道具材料，避免"10C光辉剑"被材料词带走）
  if (/光辉.{0,5}(剑|刀|弓|法装|轻装|回力|盾|铠|靴|帽|衣|长袍|袍|杖|镰|防)|10[Cc].{0,4}(剑|刀|弓|法|盾|靴|铠|武器|防具|装备|套|回力|轻装|袍|杖)|女王之刃|裂空|圣甲|骑装|重装|铠甲|头盔|盾牌|披风|之盾|火龙盾/.test(t))
    return "人物装备";

  // 7. 道具·消耗品 / 材料（含宝石、药、矿、配方等；放在游戏币之前）
  if (/尸体|血泉|魔泉|涌泉|血瓶|魔瓶|血丹|药水|药剂(?!师)|回复药|生命力回复|一瓶|精华|龙珠|灵之匣|神炼|强化石|光辉之心|光辉心|卷轴|符文|碎片|时水|宝石|白金条|钢条|金条|铁条|铜条|矿条|喜马拉斯|\d+\s*矿|材料|月木|银块|栅栏|物品袋|打卡器|配方|结构图|图纸|彩牛/.test(t))
    return "道具";

  // 8. 游戏币（消耗品已在上面摘走，这里可放宽）
  if (/魔币|金币|游戏币|元宝|MB\s*\d/.test(t)) return "游戏币";

  // 9. 宠物·具体名称（没写属性结构、也不是材料的生物）
  if (/迷你龙|布雷欧|希特拉|波加|帝皇|皮卡|奇美拉|史莱姆|幽冥之灵|树化石|地狱使者|机械核|气功蛋|分身|鄂牙|螳螂|兔子|蓝鼠|水蓝鼠|炎龙|哥布林|巨人|骷髅|冰牛|风牛|黑蛇|红鬼|白鬼|蓝鬼|青鬼|黄鬼|咒灵|不死系|庞物|宠物|改迷你|改红|改白|改蓝|改杀|改炎|改造|王八|帝皇龙|布丁|玄武|青龙|白虎|朱雀|水龙|裁判队|绿三头|三头|散步/.test(t))
    return "宠物";

  // 10. 人物装备·宽泛
  if (/装备|武器|防具|铠甲|靴|盾|披风|手套|10[Cc]|9[Cc]|光辉|黑龙|衣服|鞋|帽子|长袍|9紫|紫装|轻装|时装/.test(t))
    return "人物装备";

  // 11. 兜底：游戏账号
  return "游戏账号";
}
