// 魔力宝贝怀旧服 闲鱼商品分类器（分层版）。
//
// 闲鱼搜索里"账号 / 宠物 / 装备 / 道具"经常混在一起，标题是唯一可靠信号。
// 难点：一条商品常同时提到多类词（如"宠物血泉"既有"宠物"又有"血泉"）。
// 解决办法 = 按"卖的主体是什么"分层，顺序至关重要：
//   1) 服务/虚拟充值（代练 / 点卡 / 游戏币）先摘出去
//   2) 首饰
//   3) 强账号信号（连证/龙证/坯子/双修/多ID）——角色号即便带宠也算账号
//   4) 宠物·结构信号（档位/招牌/树海/纯血加点/X只）——"在卖一只生物"
//   5) 人物装备·具体（光辉剑/10C法装/女王之刃…）
//   6) 道具·消耗品与材料（血泉/矿/材料/光辉之心/尸体…）
//   7) 宠物·具体名称（迷你龙/史莱姆/哥布林/改造X…）——兜住没写属性结构的生物
//   8) 人物装备·宽泛（装备/10C/光辉/靴/盾…）
//   9) 兜底 = 游戏账号
export const CATEGORIES = [
  "游戏账号", "游戏币", "人物装备", "首饰", "宠物", "道具", "代练代肝", "点卡充值",
];

export function classify(title) {
  // 卖家常在标题尾部塞"搜索关联词：金币 点卡 装备 …"的关键词广告，会污染分类，
  // 先按这些标记截掉尾巴，只用真正描述商品的前半段。
  const t = String(title || "").split(/搜索关联词|搜索关键|关联词|搜索词|tag[:：]|标签[:：]/)[0];

  // 1. 代练代肝
  if (/代练|代肝|代打|代刷|陪练|包升|升级服务|托管|代冲|代烧|代做|代工|带练|带任务|带做|跟车|接任务|代过|刷声望|砍村服务|带砍村|带砍/.test(t))
    return "代练代肝";

  // 2. 点卡充值
  if (/点卡|时长|月卡|季卡|年卡|直充|代充|充值|激活码|新手卡|易玩通|点数直/.test(t))
    return "点卡充值";

  // 3. 游戏币（要求"魔币"附近有数量/现货等，避免被卖家罗列的关键词带走）
  if (/[\d万wWkK千亿]\s*魔币|魔币\s*(\d|现货|出售|专卖|批发|秒|低价|大量|无限|车|组)|金币|游戏币|元宝/.test(t))
    return "游戏币";

  // 4. 首饰
  if (/戒指|项链|手镯|手链|耳环|耳坠|首饰|饰品|之饰|护身符|魔戒|圣戒/.test(t))
    return "首饰";

  // 5. 强账号信号（结构性，优先于宠物，避免"带宠物的角色号"误判）
  if (/连证|龙证|大连证|单证|\d+\s*连(?!击)|\d+\s*ID|\d+\s*id|坯子|胚子|修理号|生产号|过户|小龙证|大生产账号|个人证/.test(t))
    return "游戏账号";

  // 6. 宠物·结构信号（"在卖一只生物"——档位/招牌/树海/纯加点/数量）
  if (/档位|档次|树海|招牌|大赛|大奖|纯血加点|纯魔加点|纯敏加点|混敏|不爆[点敏]|散步|\d+\s*只|单只一|一队.{0,6}(龙|鬼|牛|蛇|龟|兔|鼠|螂|布林|史莱姆|马|猪|鸟)/.test(t))
    return "宠物";

  // 7. 人物装备·具体（具体装备名，先于道具，避免"10C光辉剑"被材料词带走）
  if (/光辉.{0,5}(剑|刀|弓|法装|轻装|回力|盾|铠|靴|帽|衣|长袍|杖|镰|防)|10[Cc].{0,4}(剑|刀|弓|法|盾|靴|铠|武器|防具|装备|套|回力|轻装)|女王之刃|裂空|圣甲|法装|骑装|重装|铠甲|头盔|盾牌|披风/.test(t))
    return "人物装备";

  // 8. 道具·消耗品 / 材料
  if (/尸体|血泉|魔泉|涌泉|血瓶|魔瓶|药水|药剂(?!师)|精华|龙珠|灵之匣|神炼|强化石|光辉之心|光辉心|卷轴|符文|碎片|时水|白金条|钢条|金条|铁条|铜条|矿条|喜马拉斯|[\d]+\s*矿|材料|月木|银块|栅栏|物品袋|打卡器|时装|露比的衣服|经验值|彩牛/.test(t))
    return "道具";

  // 9. 宠物·具体名称（没写属性结构、也不是材料的生物）
  if (/迷你龙|布雷欧|希特拉|波加|帝皇|皮卡|奇美拉|史莱姆|幽冥之灵|树化石|地狱使者|机械核|气功蛋|分身|鄂牙|螳螂|兔子|蓝鼠|水蓝鼠|炎龙|哥布林|巨人|骷髅|冰牛|风牛|黑蛇|红鬼|白鬼|蓝鬼|青鬼|黄鬼|咒灵|不死系|庞物|宠物|改迷你|改红|改白|改蓝|改杀|改炎|改造|王八|帝皇龙|露比|布丁|希特|玄武|青龙|白虎|朱雀/.test(t))
    return "宠物";

  // 10. 人物装备·宽泛
  if (/装备|武器|防具|铠甲|靴|盾|披风|手套|10[Cc]|9[Cc]|光辉|黑龙/.test(t))
    return "人物装备";

  // 11. 兜底：游戏账号
  return "游戏账号";
}
